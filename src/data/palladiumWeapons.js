// Helper functions
export const getWeaponByName = (name) => {
  return palladiumWeapons.find((weapon) => weapon.name === name);
};

export const getWeaponsByCategory = (category) => {
  return palladiumWeapons.filter((weapon) => weapon.category === category);
};

export const getWeaponsByType = (type) => {
  return palladiumWeapons.filter((weapon) => weapon.type === type);
};

export const getMeleeWeapons = () => {
  return palladiumWeapons.filter(
    (weapon) => !weapon.range || weapon.range === null
  );
};

export const getRangedWeapons = () => {
  return palladiumWeapons.filter((weapon) => weapon.range && weapon.range > 0);
};

export const getWeaponsByStrength = (characterPS) => {
  return palladiumWeapons.filter(
    (weapon) => characterPS >= (weapon.strengthRequired || 0)
  );
};
